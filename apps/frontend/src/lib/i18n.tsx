export type Translations = typeof T['en'];

// ─── Context ────────────────────────────────────────────────────────────────
type CtxType = { lang: Lang; setLang: (l: Lang) => void; t: Translations };

const Ctx = createContext<CtxType>({
  lang: 'en',
  setLang: () => {},
  t: T.en,
});

const STORAGE_KEY = 'unilms-lang';

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    try {
      const stored = localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (stored && (stored === 'en' || stored === 'ru' || stored === 'kz')) {
        setLangState(stored);
      }
    } catch {}
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch {}
  };

  return (
    <Ctx.Provider value={{ lang, setLang, t: T[lang] as Translations }}>
      {children}
    </Ctx.Provider>
  );
}

/** Access current language, setter, and translation object */
export function useLanguage() {
  return useContext(Ctx);
}

/** Shorthand — returns just the translation object */
export function useT() {
  return useContext(Ctx).t;
}
